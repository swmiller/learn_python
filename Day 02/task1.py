# This is a simple Python script to demonstrate string indexing
text = "Hello"
print(text[0])  # Output: H
print(text[4])  # Output: o
print(text[-1])  # Output: o (get last chacter)

# Integer: Whole numbers (positive and negative)
integer_num = 10
print(integer_num)  # Output: 10
print(type(integer_num))  # Output: <class 'int'>

# Integer: Whole numbers (positive and negative) with underscores for readability
integer_num = 100_000
print(integer_num)  # Output: 100000

# Float: Decimal numbers (positive and negative)
float_num = 10.5
print(float_num)  # Output: 10.5
print(type(float_num))  # Output: <class 'float'>

# Boolean: True or False values
boolean_value = True    
print(boolean_value)  # Output: True
print(type(boolean_value))  # Output: <class 'bool'>