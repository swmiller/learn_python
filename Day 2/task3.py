# 1. Arithmetic Operators
a = 10
b = 3

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", a / b) # This will give a float result

# Floor Division
print("Floor Division:", a // b) # This will give the largest integer less than or equal to the division result

# Modulus
print("Modulus:", a % b)

# Exponentiation
print("Exponentiation:", a ** b)

# PEMDAS - Parentheses, Exponents, Multiplication/Division, Addition/Subtraction
# Example demonstrating PEMDAS

# Parentheses
result1 = (2 + 3) * 4  # Parentheses first, then multiplication
print("Result with Parentheses (2 + 3) * 4:", result1)

# Exponents
result2 = 2 ** 3 + 1  # Exponentiation first, then addition
print("Result with Exponents 2 ** 3 + 1:", result2)

# Multiplication and Division (left to right)
result3 = 10 / 2 * 3  # Division and multiplication from left to right
print("Result with Multiplication/Division 10 / 2 * 3:", result3)

# Addition and Subtraction (left to right)
result4 = 10 - 2 + 3  # Subtraction and addition from left to right
print("Result with Addition/Subtraction 10 - 2 + 3:", result4)

